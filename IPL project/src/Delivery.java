public class Delivery {
    private int matchId;
    private String balls;
    private int extraRuns;
    private String battingTeam;
    private int batsmenRuns;
    private int bowlerTotalRuns;
    private String bowlerName;
    private String batsmenName;

    public int getMatchId() {
        return matchId;
    }

    public void setMatchId(int matchId) {
        this.matchId = matchId;
    }

    public int getExtraRuns() {
        return extraRuns;
    }

    public void setExtraRuns(int extraRuns) {
        this.extraRuns = extraRuns;
    }

    public String getBattingTeam() {
        return battingTeam;
    }

    public void setBattingTeam(String battingTeam) {
        this.battingTeam = battingTeam;
    }

    public int getBatsmenRuns() {
        return batsmenRuns;
    }

    public void setBatsmenRuns(int batsmenRuns) {
        this.batsmenRuns = batsmenRuns;
    }

    public int getBowlerTotalRuns() {
        return bowlerTotalRuns;
    }

    public void setBowlerTotalRuns(int bowlerTotalRuns) {
        this.bowlerTotalRuns = bowlerTotalRuns;
    }

    public String getBowlerName() {
        return bowlerName;
    }

    public void setBowlerName(String bowlerName) {
        this.bowlerName = bowlerName;
    }

    public String getBatsmenName() {
        return batsmenName;
    }

    public void setBatsmenName(String batsmenName) {
        this.batsmenName = batsmenName;
    }

}


